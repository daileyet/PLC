package com.tdo1szh.scanning.beans;

import java.io.Serializable;

public class CommonResult implements Serializable {
    private int result;

    public int getResult() {
        return result;
    }

    public void setResult(int result) {
        this.result = result;
    }

    public boolean isSuccess() {
        return result == 0;
    }
}
