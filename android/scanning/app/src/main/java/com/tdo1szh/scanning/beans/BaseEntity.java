package com.tdo1szh.scanning.beans;

public class BaseEntity<T> extends CommonResult {
    private T data;

    public T getData() {
        return data;
    }

    public void setData(T data) {
        this.data = data;
    }

    @Override
    public String toString() {
        return "BaseEntity{" +
                "result=" + getResult() +
                ", data=" + data +
                '}';
    }
}
